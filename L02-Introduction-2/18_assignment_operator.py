# Assignment Operators Demo

# Normal assignment
x = 10
print("Initial value of x:", x)

# Add and assign
x += 5   # x = x + 5
print("After x += 5:", x)

# Subtract and assign
x -= 3   # x = x - 3
print("After x -= 3:", x)

# Multiply and assign
x *= 2   # x = x * 2
print("After x *= 2:", x)

# Divide and assign
x /= 4   # x = x / 4
print("After x /= 4:", x)

# Modulus and assign
x %= 3   # x = x % 3
print("After x %= 3:", x)

# Exponent and assign
x **= 2  # x = x ** 2
print("After x **= 2:", x)

# Floor division and assign
x //= 2  # x = x // 2
print("After x //= 2:", x)


'''
Q. Quick shorthands
- = basic assignment.
- +=, -=, *=, /= → update and assign.
- %= modulus, **= exponent, //= floor divide.
- Immutables → new object.
- Mutables → often updated in-place.
'''
