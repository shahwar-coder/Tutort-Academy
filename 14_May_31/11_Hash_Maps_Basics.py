'''
Q1. What is a hash map in simple words?
Ans:
A hash map is like a locker system where each locker has a special number (key) and stores something inside (value).
'''
# Example:
# "name" → "Shahwar"
# "age"  → 22



'''
Q2. What does a key do in a hash map?
Ans:
A key helps you find the value quickly, just like a roll number helps find a student’s locker.
'''
# Example:
# Using key "age" gives you the value 22 immediately.



'''
Q3. Why are hash maps fast?
Ans:
Because they know exactly which “box” to jump to using a hash function, instead of searching everywhere.
'''
# Example:
# Instead of checking all lockers, it goes straight to box 31.



'''
Q4. What is stored inside a hash map?
Ans:
Pairs of information called key → value.
'''
# Example:
# "fruit" → "apple"
# "id"    → 102



'''
Q5. What everyday problem can a hash map solve?
Ans:
Counting how many times something appears, without checking everything again and again.
'''
# Example:
# ["a","b","a"] → {"a":2,"b":1}



'''
Q6. Why do we use hash maps in coding?
Ans:
Because they make searching, adding, or deleting items very quick and simple.
'''
# Example:
# Check if "apple" exists → O(1), done instantly.
